print("Terdapat 2 variabel a dan b")
a = -4
b = -2
print("a =",a)
print("b =",b)
print("_"*50)
#fungsi abs
print("Tentukan nilai mutlak dari a dan b")
a = abs(a)
b = abs(b)
print('|a| =',a)
print('|b| =',b)
print("_"*50)
#fungsi min, max
print("Tentukan mana yang lebih besar antara a dan b")
min = min(a,b)
max = max(a,b)
print(max,'lebih besar dari pada',min)
print("_"*50)
#fungsi pow
print("Tentukan hasil dari a pangkat b")
c = pow(a,b)
print(a,'pangkat',b,'=',c)
print("_"*50)